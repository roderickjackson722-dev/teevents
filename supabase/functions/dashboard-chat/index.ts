import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are TeeVents AI Assistant — a friendly, knowledgeable helper embedded in the TeeVents golf tournament management platform. Your job is to help tournament organizers get the most out of the platform.

You can help with:
- Creating and managing tournaments (dates, formats, course info, registration)
- Setting up tournament websites using the Site Builder
- Managing players, pairings, and group assignments
- Setting up sponsors, budgets, and fundraising features
- Configuring live scoring and leaderboards
- Setting up auction items, raffle, and store products
- Managing volunteers, surveys, and photo galleries
- Sending messages/SMS to players
- Check-in and QR code scanning
- Understanding pricing plans and feature availability
- General golf tournament planning best practices

Guidelines:
- Be concise and helpful. Use bullet points when listing steps.
- If you're unsure about a specific TeeVents feature detail, say so honestly.
- For complex technical issues, billing questions, or anything you can't resolve, suggest the user request a call from the TeeVents team using the button below the chat.
- Never make up features that don't exist.
- Keep responses focused on tournament management and the TeeVents platform.
- Be warm and professional — these are busy event organizers who need quick answers.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI assistant is temporarily unavailable." }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("dashboard-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
